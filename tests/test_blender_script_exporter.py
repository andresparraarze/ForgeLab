def test_box_mesh_becomes_cube_primitive():
    flat, idx = _box_positions(sx=3.0, sy=3.0, sz=3.0)
    mesh = Node(
        id="m",
        type=NODE_MESH,
        props=Mesh(name="box", primitives=[Primitive(positions=flat, indices=idx)]).model_dump(),
    )
    src = _export([_scene(), mesh, _object("Box", "m")])
    assert "bpy.ops.mesh.primitive_cube_add(size=2.0)" in src
    assert "from_pydata" not in src  # detected, not raw soup
    ast.parse(src)


def test_cylinder_mesh_becomes_cylinder_primitive():
    flat = _cylinder_positions(radius=1.5, depth=4.0, n=16, axis=2)
    mesh = Node(
        id="m",
        type=NODE_MESH,
        props=Mesh(name="cyl", primitives=[Primitive(positions=flat)]).model_dump(),
    )
    src = _export([_scene(), mesh, _object("Cyl", "m")])
    assert "bpy.ops.mesh.primitive_cylinder_add(vertices=16" in src
    assert "from_pydata" not in src
    ast.parse(src)


def test_unrecognized_mesh_falls_back_to_raw():
    # A flat triangle: 3 verts, not a box/cylinder/sphere.
    mesh = Node(
        id="m",
        type=NODE_MESH,
        props=Mesh(
            name="tri",
            primitives=[Primitive(positions=[0, 0, 0, 1, 0, 0, 0, 1, 0], indices=[0, 1, 2])],
        ).model_dump(),
    )
    src = _export([_scene(), mesh, _object("Tri", "m")])
    assert "from_pydata" in src
    assert "primitive_cube_add" not in src
    ast.parse(src)


