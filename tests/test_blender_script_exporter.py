"""BlenderScriptExporter: ForgeLab threed IR -> a runnable Blender bpy script."""

import ast
import math

from forgelab.exporters.threed.blender_script import BlenderScriptExporter, _camera_location
from forgelab.spec import (
    DocumentMeta,
    Domain,
    ForgeDocument,
    Material,
    Mesh,
    Node,
    Object3D,
    Primitive,
    Transform,
)
from forgelab.spec.threed import NODE_MATERIAL, NODE_MESH, NODE_OBJECT, NODE_SCENE
from forgelab.spec.version import SPEC_VERSION


def _identity_transform():
    return Transform(
        translation=[0.0, 0.0, 0.0], rotation=[0.0, 0.0, 0.0, 1.0], scale=[1.0, 1.0, 1.0]
    )


def _doc(nodes, name="TestScene"):
    return ForgeDocument(
        forgelab_version=SPEC_VERSION,
        domain=Domain.THREED,
        meta=DocumentMeta(name=name, generator="test"),
        nodes=nodes,
    )


def _box_positions(cx=0.0, cy=0.0, cz=0.0, sx=2.0, sy=2.0, sz=2.0):
    hx, hy, hz = sx / 2, sy / 2, sz / 2
    pts = [
        (cx - hx, cy - hy, cz - hz),
        (cx + hx, cy - hy, cz - hz),
        (cx + hx, cy + hy, cz - hz),
        (cx - hx, cy + hy, cz - hz),
        (cx - hx, cy - hy, cz + hz),
        (cx + hx, cy - hy, cz + hz),
        (cx + hx, cy + hy, cz + hz),
        (cx - hx, cy + hy, cz + hz),
    ]
    flat = [c for p in pts for c in p]
    # Two triangles so the mesh has indexed faces (front face only is enough).
    return flat, [0, 1, 2, 0, 2, 3]


def _cylinder_positions(radius=1.0, depth=4.0, n=12, axis=2):
    """Axis-aligned cylinder: two rings of n points + two cap centres."""
    import math

    half = depth / 2
    pts = []
    for plane in (-half, half):
        for i in range(n):
            a = 2 * math.pi * i / n
            ring = [radius * math.cos(a), radius * math.sin(a)]
            p = [0.0, 0.0, 0.0]
            p[axis] = plane
            others = [k for k in (0, 1, 2) if k != axis]
            p[others[0]] = ring[0]
            p[others[1]] = ring[1]
            pts.append(tuple(p))
    for plane in (-half, half):
        c = [0.0, 0.0, 0.0]
        c[axis] = plane
        pts.append(tuple(c))
    return [v for p in pts for v in p]


def _scene():
    return Node(id="scene", type=NODE_SCENE, props={"name": "TestScene"})


def _material():
    return Node(
        id="mat_red",
        type=NODE_MATERIAL,
        props=Material(
            name="vermilion", base_color=[0.8, 0.1, 0.1, 1.0], metallic=0.0, roughness=0.4
        ).model_dump(),
    )


def _object(name, mesh_id):
    return Node(
        id=f"obj_{name}",
        type=NODE_OBJECT,
        props=Object3D(name=name, transform=_identity_transform(), mesh=mesh_id).model_dump(),
    )


def _export(nodes, name="TestScene"):
    return BlenderScriptExporter().from_ir(_doc(nodes, name)).decode("utf-8")


