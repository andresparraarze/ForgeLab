# valid Python output
# --------------------------------------------------------------------------- #
def test_output_is_valid_python():
    flat, idx = _box_positions()
    mesh = Node(
        id="m",
        type=NODE_MESH,
        props=Mesh(
            name="cube", primitives=[Primitive(positions=flat, indices=idx, material="mat_red")]
        ).model_dump(),
    )
    src = _export([_scene(), _material(), mesh, _object("Cube", "m")])
    ast.parse(src)  # raises SyntaxError if the script is not valid Python
    assert src.startswith("# Generated by ForgeLab")
    assert "import bpy" in src


